function add(a, b) {
    /**
     * Returns the addition of 2 integers.
     *
     * @param {number} a The first integer.
     * @param {number} b The second integer.
     * @returns {number} The sum of a and b.
     */
    return a + b;
  }
  
  console.log(add(4, 85));
  console.log(add(93, -4));
  console.log(add(0, 8));  